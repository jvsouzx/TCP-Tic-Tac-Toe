    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JToggleButton[][] matrix = returnMatrix();
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                if (matrix[i][j].getText().equals("0")) {
                    matrix[i][j].setEnabled(false);
                } else {
                    matrix[i][j].setEnabled(true);
                }
            }
        }
        
        str_matrix = "";
        
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                str_matrix = str_matrix + matrix[i][j].getText() + ",";
            }
            str_matrix = str_matrix + "/";
        }
        
        try{
            Socket PlayerOToServ = new Socket("localhost", 4040);
           
            ObjectOutputStream out =  new ObjectOutputStream(PlayerOToServ.getOutputStream());
            out.flush();
            out.writeUTF(str_matrix);
            out.writeUTF(turn);
            out.close();
            
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro:" + e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

